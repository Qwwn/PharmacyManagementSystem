    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         int checkCompanyExist = 0;
        String name = txtCompany.getText();
        if(name.equals("")){
            JOptionPane.showMessageDialog(null, "Company Name field is required");
        } else {
            try{
                Connection con = ConnectionProvider.getCon();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from company where name='"+name+"'");
                while(rs.next()){
                    txtCompany.setEditable(false);
                    txtName.setText(rs.getString("name"));
                    txtAddress.setText(rs.getString("address"));
                    txtPhone.setText(rs.getString("phone"));
                    checkCompanyExist = 1;
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            if (checkCompanyExist==0){
                JOptionPane.showMessageDialog(null,"Company name does not exist");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

