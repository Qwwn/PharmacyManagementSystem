    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
    String name = txtName.getText();
    String address = txtAddress.getText();
    String phone = txtPhone.getText();

    if (name.equals("")) {
        JOptionPane.showMessageDialog(null, "Company name field is required");
    } else {
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("update company set name=?,address=?,phone=? where name=?");
            ps.setString(1, name);
            ps.setString(2, address);
            ps.setString(3, phone);
            ps.setString(4, txtCompany.getText());
            int rowsUpdated = ps.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Company updated successfully");
                setVisible(false);
                new updatecompany().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Company name does not exist");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

